$("#type").change(function () {
    let serarchText = $("#search-text");
    if ($(this).val() == 1) {
        serarchText.prop("type", "text");
        serarchText.attr("placeholder", "请输入您要查找的学校");
    } else {
        serarchText.prop("type", "number");
        serarchText.attr("placeholder", "请输入您的分数");
    }
})

$("#select").click(function () {
    let type = $("#type").val();
    let serarchText = $("#search-text").val();
    if (type == 1) {
        score = oldScore;
        score = score.filter(v => {
            return v[3].indexOf(serarchText) != -1;
        });
        show(score);
        scoreUpdateView();
        subjectUpdateView();
    } else {
        score = oldScore;
        score = score.filter(v => {
            return parseInt(v[4]) <= parseInt(serarchText);
        });
        show(score);
        scoreUpdateView();
        subjectUpdateView();
    }
    return false;
})