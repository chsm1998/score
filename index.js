function show(score) {
    let tb = document.getElementsByTagName("tbody")[0];
    let content = "";
    score.forEach(v => {
        content += "<tr>";
        v.forEach(s => {
            content += "<td>" + s + "</td>";
        })
        content += "</tr>"
    })
    tb.innerHTML = content;
}

function scoreUpdateView() {
    let scoreValue = $("#scores").val();
    if (scoreValue == 1) {
        show(score.sort((x, y) => x[4] - y[4]))
    }
    if (scoreValue == 0) {
        show(score.sort((x, y) => y[4] - x[4]))
    }
}

function subjectUpdateView() {
    let subjectValue = $("#subject").val();
    if (subjectValue == 1) {
        show(score.filter(v => v[1].indexOf("文史") != -1))
    }
    if (subjectValue == 0) {
        show(score.filter(v => v[1].indexOf("理工") != -1))
    }
}

$("#subject").change(function () {
    scoreUpdateView();
    subjectUpdateView();
})

$("#scores").change(function () {
    scoreUpdateView();
    subjectUpdateView();
})

